    private void scheduleCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesButtonActionPerformed
        // TODO add your handling code here:
        String courseID = (String)(selectCourseComboBox.getSelectedItem());
        String selectedStudent = (String)(selectStudentComboBox.getSelectedItem());
        int studentIndex = selectStudentComboBox.getSelectedIndex();

        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(studentIndex).getStudentID();

        int seats = CourseQueries.getCourseSeats(currentSemester, courseID);
        int count = ScheduleQueries.getScheduledStudentCount(currentSemester, courseID);

        String status = "S";
        if (seats <= count){
            status = "W";
            scheduleCoursesLabel.setText(selectedStudent+" is wailisted");
        }
        else{
             scheduleCoursesLabel.setText(selectedStudent+" has been enrolled in the class");
        }

        Timestamp time = new Timestamp(0);

        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(currentSemester, studentID);
        ScheduleEntry entry = new ScheduleEntry(currentSemester, courseID, studentID, status, time);

        for (ScheduleEntry entry1: schedules) {
            if (entry1.getCourseCode().equals(entry.getCourseCode())) {
                scheduleCoursesLabel.setText(selectedStudent+ " is already scheduled");
                return;
            }
        }

        ScheduleQueries.addScheduleEntry(entry);
        rebuildSelectStudentComboBox();
    }//GEN-LAST:event_scheduleCoursesButtonActionPerformed

