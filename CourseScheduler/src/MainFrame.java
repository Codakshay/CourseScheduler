    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
        
        String selectedStudent = (String)(dropStudentComboBox.getSelectedItem());
        
        if (selectedStudent == null) {
            return;
        }
        
        int studentIndex = dropStudentComboBox.getSelectedIndex();

        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = students.get(studentIndex).getStudentID();
        
        StudentQueries.dropStudent(studentID);
        dropStudentAdminTextArea.setText(selectedStudent + " has been dropped. ");
        ArrayList<String> allSemesters = getAllSemesters();
        
        for (String semester: allSemesters) {            
            
            ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            
            for (int i = 0; i < studentSchedule.size(); i++) {
                if (studentSchedule.get(i).getStatus().equals("W")) {
                    dropStudentAdminTextArea.append("\n" + selectedStudent + ", " + studentSchedule.get(i).getCourseCode() + ", has been removed from the waitlist of " + studentSchedule.get(i).getStudentID() + " for semester " + semester);
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, studentSchedule.get(i).getStudentID());
                    studentSchedule.remove(i);
                }
            }

            ArrayList<String> courseCodes = new ArrayList<>();
            for (ScheduleEntry scheduleEntry: studentSchedule) {
                courseCodes.add(scheduleEntry.getStudentID());
                dropStudentAdminTextArea.append("\n" + selectedStudent + ", " +scheduleEntry.getCourseCode()  + ", has been removed from " + scheduleEntry.getStudentID() + " for semester " + semester);                
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, scheduleEntry.getStudentID());
                       
            }
            ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        
            displayCoursesTextArea.setText("Course Code\tSeats\tDescription\n");
            for (CourseEntry course: courses) {
            displayCoursesTextArea.append("\n" + course.getCourseCode() + "\t" + course.getSeats() + "\t" + course.getCourseDescription());
            }
            
            for (String courseCode: courseCodes) {
                
                ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduledStudentsByCourse(semester, courseCode);
                for (ScheduleEntry scheduleEntry: schedules) {
                    if (scheduleEntry.getStatus().equals("W")) {
                        ScheduleQueries.updateScheduleEntry(scheduleEntry.getCourseCode(),"S",courseCode );
                        
                        dropStudentAdminTextArea.append("\n" + scheduleEntry.getCourseCode() + ", has been enrolled in " + courseCode + " for semester " + semester);
                        
                        ArrayList<CourseEntry> courses2 = CourseQueries.getAllCourses(currentSemester);
        
                        displayCoursesTextArea.setText("Course Code\tSeats\tDescription\n");
                        for (CourseEntry course: courses2) {
                        displayCoursesTextArea.append("\n" + course.getCourseCode() + "\t" + course.getSeats() + "\t" + course.getCourseDescription());
                        }
                        break;
                    }
                }
            }
            dropStudentAdminTextArea.append("\n");
        }
                
    
        rebuildSelectStudentComboBox();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

